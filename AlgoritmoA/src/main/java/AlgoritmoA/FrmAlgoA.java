    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        int ancho, alto;
        ancho = Integer.parseInt(this.jTextField1.getText());
        alto = Integer.parseInt(this.jTextField2.getText());
        Operaciones o;
        o = new Operaciones(ancho, alto);
        n = new jPanNodo[ancho][alto];
        o.crearMatriz();
        o.asigIniFinClass();
        o.asigParedesClass();
        jPanel2.setSize(82 * ancho, 110 * alto);
        jPanel2.setLocation(50, 70);
        jPanel1.add(jPanel2);
        jPanel1.revalidate();
        jPanel1.repaint();
        for (int i = 0; i < ancho; i++) {
            for (int j = 0; j < alto; j++) {
                n[i][j] = new jPanNodo();
                n[i][j].asignarDatos(o.getMatriz()[i][j].getF(), o.getMatriz()[i][j].getH(), o.getMatriz()[i][j].getG(), "(" + o.getMatriz()[i][j].getX() + "," + o.getMatriz()[i][j].getY() + ")");
                if (o.getMatriz()[i][j].getA() == 'I') {
                    n[i][j].pintaPanel(Color.yellow);
                }
                if (o.getMatriz()[i][j].getA() == 'S') {
                    n[i][j].pintaPanel(Color.GREEN);
                }
                if (o.getMatriz()[i][j].getA() == '#') {
                    n[i][j].pintaPanel(Color.black);
                }
                this.jPanel2.add(n[i][j]);
                this.jPanel2.setLayout(new FlowLayout());
                this.jPanel2.revalidate();
                this.jPanel2.repaint();
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

